Σε πιστοποίηση των ανωτέρω συντάχθηκε το παρόν σε τέσσερα (4) όμοια πρωτότυπα, εκ των οποίων τρία (3) λαμβάνει ο Αναθέτων και ένα (1) ο Ανάδοχος και, αφού διαβάστηκε και βεβαιώθηκε το περιεχόμενό του από τα συμβαλλόμενα μέρη, υπογράφεται ως ακολούθως:
</p>
