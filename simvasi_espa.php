Ο Ανάδοχος, εάν δεν εκπληρώνει τις συμβατικές υποχρεώσεις του,  κηρύσσεται έκπτωτος μετά από εισήγηση του Επιστημονικού Υπευθύνου του Έργου- της Πράξης και απόφαση της Δ.Ε. του Αναθέτοντα και της Ε.Δ.Ε.Λ.
</p>
<p>
Η αδυναμία του Αναδόχου να παραδώσει εγκαίρως και προσηκόντως, έστω και ένα από τα παραδοτέα, που περιγράφονται στην παρούσα σύμβαση, αποτελεί λόγο έκπτωσης, η οποία επέρχεται από την επομένη της επιδόσεως του σχετικού εγγράφου -αναγγελία στον Ανάδοχο.
</p>

<p style="font-size:12pt; text-decoration: underline; "><strong>7. ΑΠΟΤΕΛΕΣΜΑΤΑ ΚΑΤΑΓΓΕΛΙΑΣ - ΕΚΠΤΩΣΗΣ</strong></p>

<p>
<strong>7.1.</strong> Σε περίπτωση, διακοπής της Πράξης για οποιονδήποτε λόγο ή καταγγελίας της σύμβασης από τον Αναθέτοντα, θα καταβάλλεται στον Ανάδοχο αποκλειστικά και μόνο η αμοιβή που αντιστοιχεί στο έργο, που θα έχει εκτελεστεί και παραδοθεί από αυτόν μέχρι την ημέρα διακοπής –καταγγελίας της παρούσας σύμβασης - έκπτωσης,  με την προϋπόθεση της οριστικής παραλαβής του.
</p>
<p>
<strong>7.2.</strong>Στις ανωτέρω περιπτώσεις ο Ανάδοχος υποχρεούται αμέσως μετά τη λύση - καταγγελία της παρούσας- έκπτωση, να απέχει από οποιαδήποτε ενέργεια σχετίζεται με την περιγραφόμενη στην παρούσα δραστηριότητά του και να παραδώσει στον Αναθέτοντα εντός τριών (3) εργασίμων ημερών τα αποτελέσματα του έργου, που εκτέλεσε μέχρι την καθ’ οιονδήποτε τρόπο λήξη της σύμβασης, ως και να επιστρέψει στον Αναθέτοντα πάν ό,τι διέθεσε εις αυτόν ο τελευταίος προς διευκόλυνσή του κατά την εκτέλεση του έργου, άλλως ο Ανάδοχος υποχρεούται σε κάθε θετική ή αρνητική ζημία, που ήθελε επέλθει, άμεσα ή έμμεσα, στον Αναθέτοντα, από δυστροπία, άρνηση ή αμέλεια του ως προς την επιστροφή των ανωτέρω.  
</p>

<p style="font-size:12pt; text-decoration: underline; "><strong>8. ΠΝΕΥΜΑΤΙΚΑ ΔΙΚΑΙΩΜΑΤΑ</strong></p>
<p>
<strong>8.1.</strong> Ο Ανάδοχος μεταβιβάζει και εκχωρεί, ρητά και ανέκκλητα, στον Αναθέτοντα κάθε μορφής δικαιώματά του (πνευματικά, ηθικά και συγγενικά) επί του Έργου, που είναι αναγκαία για την εκπλήρωση των όρων και του σκοπού της παρούσας σύμβασης, ως και των εν γένει σκοπών και επιδιώξεων του Αναθέτοντα, όπως αυτά προκύπτουν από τις κείμενες διατάξεις περί πνευματικής ιδιοκτησίας και ιδία από τα άρθρα 3 και 4 του Ν. 2121/1993.
</p>
<p>
<strong>8.2.</strong> Η ως άνω μεταβίβαση των δικαιωμάτων ισχύει για όλες τις χώρες του κόσμου και για χρονικό διάστημα ίσο με τη διάρκεια προστασίας του Έργου σύμφωνα με το Ν. 2121/1993.
</p>
<p>
<strong>8.3.</strong> Ο Αναθέτων διατηρεί το δικαίωμα της ελεύθερης μεταβίβασης των πάσης φύσεως δικαιωμάτων, που απορρέουν από την παρούσα σύμβαση σε άλλα φυσικά ή νομικά πρόσωπα στην Ελλάδα ή την αλλοδαπή.- 
</p>
<p>
<strong>8.4.</strong> Ο Αναθέτων δικαιούται να ασκήσει, κατά την κρίση του και αναλόγως των περιστάσεων και εν γένει συνθηκών, τα κάθε είδους δικαιώματά του, που απορρέουν από την εκμετάλλευση του Έργου ως περιουσιακού στοιχείου, είτε με δικά του μέσα, είτε μεταβιβάζοντας ή παραχωρώντας, αποκλειστικές ή μη, προς τούτο άδειες σε τρίτους.
</p>
<p>
<strong>8.5.</strong> Απαγορεύεται ρητά στον Ανάδοχο να παραχωρήσει οποιοδήποτε δικαίωμα σε οιονδήποτε τρίτο, ή να διαθέτει ή να χρησιμοποιεί μέρος ή το σύνολο του έργου για οιονδήποτε λόγο, χωρίς τη προηγούμενη γραπτή έγκριση του Αναθέτοντα.
</p>

<p style="font-size:12pt; text-decoration: underline; "><strong>9. ΕΜΠΙΣΤΕΥΤΙΚΟΤΗΤΑ</strong></p>

<p>
<strong>9.1.</strong> Ο Ανάδοχος, χωρίς την προηγούμενη έγγραφη συναίνεση του Αναθέτοντα, δεν αποκαλύπτει καθ’ οιονδήποτε τρόπο εμπιστευτικές πληροφορίες, που του δόθηκαν ή που ο ίδιος ανακάλυψε κατά την υλοποίηση του Έργου, ούτε κοινοποιεί στοιχεία, έγγραφα και πληροφορίες των οποίων λαμβάνει γνώση κατά την εκτέλεση του έργου, υποχρεούται δε να μεριμνά εις τρόπον ώστε τρίτα πρόσωπα, που συνεργάζονται μαζί του (βοηθός κλπ.) να τηρήσουν την ως άνω υποχρέωση. Σε περίπτωση αθέτησης της ως άνω υποχρέωσης ο Αναθέτων δικαιούται να απαιτήσει την αποκατάσταση κάθε τυχόν θετικής ή αποθετικής ζημίας, ως και την παύση κοινοποίησης των εμπιστευτικών πληροφοριών και την παράλειψή τους στο μέλλον.
</p>
<p>
Ο Ανάδοχος δεν δύναται να προβαίνει σε δημόσιες δηλώσεις σχετικά με το Έργο χωρίς την προηγούμενη συναίνεση του Αναθέτοντα.
</p>
<p>
<strong>9.2.</strong> Ο Ανάδοχος υποχρεούται: <strong>α)</strong> Να λαμβάνει κάθε πρόσφορο μέσο και διατυπώσεις μυστικότητας, ώστε να αποφευχθεί η διαρροή και η δημοσιοποίηση, των πληροφοριών, που περιέρχονται σε γνώση του κατά την εκτέλεση του έργου, η τυχόν καταστροφή ή απώλεια των εμπεπιστευμένων σε αυτόν εγγράφων, εγχειριδίων και λοιπών πληροφοριών, στα οποία θα εμπεριέχονται οι ως άνω παραπάνω εμπιστευτικής φύσεως πληροφορίες, <strong>β)</strong> να χρησιμοποιεί τις εμπιστευτικές πληροφορίες μόνο προς το σκοπό της παρούσας σύμβασης και <strong>γ)</strong> να μην χρησιμοποιήσει την τεχνογνωσία και την εμπειρία, που θα αποκομίσει από την πρόσβαση στις εμπιστευτικές πληροφορίες, παρά μόνο για τους σκοπούς της παρούσας σύμβασης σύμφωνα προς τους ειδικότερους όρους αυτής.
</p>
<strong>9.3.</strong> Οι υποχρεώσεις, που απορρέουν από το παρόν άρθρο, εξακολουθούν να ισχύουν και να δεσμεύουν τον Ανάδοχο και μετά την καθ’ οιονδήποτε τρόπο διακοπή της συνεργασίας του με το Πανεπιστήμιο.
</p>

<p style="font-size:12pt; text-decoration: underline; "><strong>10. ΑΘΕΜΙΤΟΣ ΑΝΤΑΓΩΝΙΣΜΟΣ</strong></p>
<p>
Ο ανάδοχος καθ’ όλη την διάρκεια εκτελέσεως έργου υποχρεούται να απέχει από κάθε μορφής ανταγωνιστικές πράξεις προς τις δραστηριότητες, που θα αναπτύσσει κατά την εκτέλεση του έργου, μετερχόμενος αυτές, είτε ευκαιριακά, είτε κατ΄ επάγγελμα, είτε επ’ ονόματί του, είτε μέσω προστηθέντων ή παρένθετου προσώπου τόσο στην Ελλάδα, όσο και σε τρίτες χώρες.
</p>

<p style="font-size:12pt; text-decoration: underline; "><strong>11.  ΕΠΙΛΥΣΗ ΔΙΑΦΟΡΩΝ - ΤΕΛΙΚΕΣ ΔΙΑΤΑΞΕΙΣ</strong></p>
<P>
<strong>11.1.</strong>Η παρούσα σύμβαση διέπεται από το Ελληνικό δίκαιο και κάθε διαφορά εξ αυτής υπάγεται στην αποκλειστική αρμοδιότητα των Δικαστηρίων Πατρών.
</p>
<P>
<strong>11.2.</strong>Όλοι οι όροι της παρούσας σύμβασης, ως και των τυχόν Παραρτημάτων, που συνοδεύουν αυτή και αποτελούν αναπόσπαστο τμήμα της, είναι ουσιώδεις και σπουδαίοι, η παραβίαση δε οποιουδήποτε τούτων παρέχει το δικαίωμα στον Αναθέτοντα της μονομερούς λύσης και καταγγελίας της παρούσας σύμβασης αζημίως δι’ εαυτόν.
</p>
<P>
<strong>11.3.</strong>Κάθε τροποποίηση των όρων και συμφωνιών της παρούσας θα αποδεικνύεται αποκλειστικά και μόνο εγγράφως, αποκλειομένου κάθε άλλου αποδεικτικού μέσου, και του όρκου εισέτι.
</p>
<P>
<strong>11.4.</strong>Τυχόν ακυρότητα ενός όρου δεν επηρεάζει το κύρος της όλης σύμβασης, οι δε συμβαλλόμενοι υποχρεούνται να καλύψουν το κενό, που ενδέχεται να προκύψει από ακυρότητα όρου, ερμηνευτικά ή με συμπλήρωση της παρούσας, κατά τρόπο που να εκπληρούται ο οικονομικός σκοπός της σύμβασης.
</p>

